linguagens = ["python", "js", "c", "java", "csharp"]

# pop retirar os objetos em forma de pilha, ou seja,o último adicionado será o 1º removido, caso não infome o índice.
# Podemos passar o índice do objeto a ser removido, ex.: print(linguagens.pop(0))
print(linguagens.pop())  # csharp
print(linguagens.pop())  # java
print(linguagens.pop())  # c
print(linguagens.pop(0))  # python
print(linguagens)